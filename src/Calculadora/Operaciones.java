package Calculadora;

public class Operaciones {
    
   
    public int calulcarSuma(int a, int b){
        return a + b;
    }
    
    
    public int calcularMultiplicacion(int a, int b){
        return a*b;
    }
    
     public int calcularResta(int a, int b){
        return a-b;
    }
     
    public int caluclarDivision(int a, int b){
        return a/b;
    }
    
    
}
